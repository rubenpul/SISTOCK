    private void bot_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_buscarActionPerformed
        
        String sql;
        boolean resp;
        String tmp;

        sql = "select a.cedula,a.nombres,a.usuario,a.contrasena,a.status,a.nivel from tbl_usuario a where a.cedula = ";
        
        if (!txt_cedula.getText().equals("")){

            usuario us = new usuario(Integer.parseInt(txt_cedula.getText().trim()),sql);

            resp = us.buscar_cedula_usuario();

            if (resp){

                txt_cedula.setText(String.valueOf(us.getcedula()));
                txt_nombres.setText(String.valueOf(us.getNombres()));
                txt_login.setText(us.getusuario()) ;
                txt_password.setText(String.valueOf(us.getpassword()));
                txt_confirmar.setText(String.valueOf(us.getpassword()));

                if (us.getnivel().equals(1)){
                    radio_nivel1.setSelected(true);
                    radio_nivel2.setSelected(false);
                }
                else{
                    radio_nivel2.setSelected(true);
                    radio_nivel1.setSelected(false);
                }

                bot_guardar.setEnabled(false);
                
                if (us.getstatus()){
                    bot_baja.setEnabled(true);
                    bot_alta.setEnabled(false);
                    txt_nombres.setEnabled(true);
                    txt_login.setEnabled(true);
                    txt_password.setEnabled(true);
                    txt_confirmar.setEnabled(true);
                    bot_actualizar.setEnabled(true);
                    radio_nivel1.setEnabled(true);
                    radio_nivel2.setEnabled(true);
                }
                else{

                    txt_cedula.setEnabled(false);
                    txt_nombres.setEnabled(false);
                    txt_login.setEnabled(false);
                    txt_password.setEnabled(false);
                    txt_confirmar.setEnabled(false);
                    bot_actualizar.setEnabled(false);    
                    bot_baja.setEnabled(false);
                    bot_alta.setEnabled(true);
                    radio_nivel1.setEnabled(false);
                    radio_nivel2.setEnabled(false);
                }
            }
            else{
                //tmp= txt_cedula.getText();
                //bot_limpiar.doClick();
                //txt_cedula.setText(tmp);
                txt_nombres.requestFocusInWindow();
                bot_guardar.setEnabled(true);
            }

        }

    }//GEN-LAST:event_bot_buscarActionPerformed


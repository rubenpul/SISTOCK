    private void txt_cedulaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cedulaKeyPressed
        if (evt.getKeyCode()==10){
            bot_buscar.doClick();
        }
        
        if ( ( (evt.getKeyCode() >= 48 ) && (evt.getKeyCode() <= 57 ) ) || ( (evt.getKeyCode() >= 37 ) && (evt.getKeyCode() <= 40 ) ) || ((evt.getKeyCode() >= 96 ) && (evt.getKeyCode() <= 105 )) || (evt.getKeyCode() == 144) || (evt.getKeyCode() == 127) || (evt.getKeyCode() == 8)  || (evt.getKeyCode() == 10)){       
        }
        else{
             if (evt.getKeyCode() == 27){
         
                    bot_limpiar.doClick();
                    
             }
             else{
                JOptionPane.showMessageDialog(null, "CARACTER NO VALIDO. SOLO NÃšMEROS (1..9)", "SISTOCK - USUARIO", JOptionPane.ERROR_MESSAGE,null);
                txt_cedula.setText("");
             }   
        }

    }//GEN-LAST:event_txt_cedulaKeyPressed

    private void bot_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_guardarActionPerformed
 
        String sql;
        boolean resp;
        String password = new String(txt_password.getPassword());
        String confirm_password = new String(txt_confirmar.getPassword());
        Integer nivel = 2;
        
        if(!txt_cedula.getText().equals("") && !txt_nombres.getText().equals("") && !txt_login.getText().equals("") && !txt_password.getPassword().equals("") && !txt_confirmar.getPassword().equals("") ) {
            
            if (password.equals(confirm_password)){
                
                if (radio_nivel1.isSelected()){
                    nivel = 1;
                }
                else{
                    nivel = 2;
                }
                    
                
                sql = "insert into tbl_usuario values(";

                usuario us = new usuario(Integer.parseInt(txt_cedula.getText().trim()),txt_nombres.getText().trim(),txt_login.getText().trim(),confirm_password,nivel,true,sql);

                resp = us.actualizar_usuario();

                if (resp){
                    bot_baja.setEnabled(true);
                    bot_alta.setEnabled(false);
                    bot_guardar.setEnabled(false);
                    bot_actualizar.setEnabled(true);
                    bot_limpiar.setEnabled(true);
                }
            }
            else{
                 JOptionPane.showMessageDialog(null,"ERROR. NO HAY COINCIDENCIA EN EL PASSWORD");
            }
        }
        else{

            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA GUARDAR");
        }
    }//GEN-LAST:event_bot_guardarActionPerformed

    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed

        boolean resp;
        String sql;
        Integer nivel = 2;
        String password = new String(txt_password.getPassword());
        String confirm_password = new String(txt_confirmar.getPassword());
 
        
        if(!txt_cedula.getText().equals("") && !txt_nombres.getText().equals("") && !txt_nombres.getText().equals("") && !txt_password.getPassword().equals("") && !txt_confirmar.getPassword().equals("") ) {

            if (password.equals(confirm_password)){
            
                if (radio_nivel1.isSelected()){
                    nivel = 1;
                }
                else{
                    nivel = 2;
                }
              
            
                sql = "update tbl_usuario set contrasena = ";

                usuario us = new usuario(Integer.parseInt(txt_cedula.getText().trim()),txt_nombres.getText().trim().toUpperCase(),txt_login.getText().trim(),confirm_password,nivel,true,sql);

                resp = us.actualizar_usuario_detalle();

                if (resp){
                    bot_alta.setEnabled(false);
                }
            }
            else{
                 JOptionPane.showMessageDialog(null,"ERROR. NO HAY COINCIDENCIA EN EL PASSWORD");
            }    
        }
        else{

            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA ACTUALIZAR");
        }
    }//GEN-LAST:event_bot_actualizarActionPerformed


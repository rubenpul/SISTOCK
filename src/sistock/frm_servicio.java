    private void txt_nuevaservicioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_nuevaservicioKeyPressed
       if (evt.getKeyCode()==10){
            bot_actualizar.doClick();
        }
        
        if (evt.getKeyCode() == 27){
         
            bot_limpiar.doClick();
                    
        }
    }//GEN-LAST:event_txt_nuevaservicioKeyPressed

    private void cmb_servicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmb_servicioActionPerformed
        
        if(evt.getModifiers() != 0){
            if (cmb_servicio.getSelectedItem().equals("Seleccione...")){
                txt_nuevaservicio.setText("");
            }
            else{
                txt_nuevaservicio.setText(cmb_servicio.getSelectedItem().toString().substring(4,cmb_servicio.getSelectedItem().toString().length()).trim());
            }
        }
        
    }//GEN-LAST:event_cmb_servicioActionPerformed


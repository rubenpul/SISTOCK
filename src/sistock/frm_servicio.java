    private void bot_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_crearActionPerformed
        String sql;
        boolean resp;
        
        if(!txt_servicio.getText().equals("")) {

                servicio ser = new servicio(txt_servicio.getText().trim().toUpperCase(),2);

                resp = ser.actualizar_servicio();
                
                if (resp){
                   ser.consulta_servicio(table_servicio);
                   sql="select id_servicio,descripcion from tbl_servicio;";       
                   cmb_servicio = funciones.cargar_combo(cmb_servicio,sql,txt_servicio.getText().toUpperCase().trim());
                   

                   txt_servicio.setText("");
                   
                }
                
        }
        else{
            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA GUARDAR");
        }
    }//GEN-LAST:event_bot_crearActionPerformed


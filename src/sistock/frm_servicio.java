    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed
        boolean resp = true;
        String sql;
         
        
        if(!txt_nuevaservicio.getText().equals("") && !cmb_servicio.getSelectedItem().equals("Seleccione...") ) {

                           
                servicio ser = new servicio(txt_nuevaservicio.getText().trim().toUpperCase(),2);
                
                resp = ser.buscar_idservicio();
                
           
            
            if (resp){
                 ser = new servicio(Integer.parseInt(cmb_servicio.getSelectedItem().toString().substring(0,3).trim()),txt_nuevaservicio.getText().trim().toUpperCase(),3);

                resp = ser.actualizar_servicio_detalle();

                if (resp){
                    ser.consulta_servicio(table_servicio);
                    
                    sql="select id_servicio,descripcion from tbl_servicio;";       
        
                    cmb_servicio = funciones.cargar_combo(cmb_servicio,sql,txt_nuevaservicio.getText().trim().toUpperCase());
                    
                    txt_nuevaservicio.setText("");
                    
                }
            }
        }
        else{

            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA ACTUALIZAR");
        }
    }//GEN-LAST:event_bot_actualizarActionPerformed

